#define NUM_CASES (sizeof(TESTS) / sizeof(block_function_test_t))

int main() {
    uint8_t out[64];
    bool pass;
    unsigned int tests_passed = 0;

    printf("RUNNING BLOCK FUNCTION TESTS ...\nDIFFERENCE LINE:\n[.] - equal bytes, [*] - different bytes\n\n");

    for (unsigned i = 0; i < NUM_CASES; i++) {
        printf("[TEST] #%d\n", i+1);
        pass = true;

        chacha20_block(out, TESTS[i].key, TESTS[i].nonce, TESTS[i].block_count);

        for (unsigned j = 0; j < 64; j++) {
            if (out[j] == TESTS[i].correct[j]) {
                putc('.', stdout);
            } else {
                pass = false;
                putc('*', stdout);
            }
        }

        if (pass) {
            printf(" [PASS]\n\n");
            tests_passed++;
        } else {
            printf(" [FAIL]\n\n");
        }
    }

    printf("[RESULT] %u/%u PASSES\n", tests_passed, (unsigned int)NUM_CASES);
}
